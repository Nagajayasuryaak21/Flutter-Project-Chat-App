      _saveDataToContactsSubcollection(
        senderUser,
        recieverUserData,
        text,
        timeSent,
        recieverUserId,
        isGroupChat,
      );

      bool bl = await _saveMessageToMessageSubcollection(
        recieverUserId: recieverUserId,
        text: text,
        timeSent: timeSent,
        messageType: MessageEnum.text,
        messageId: messageId,
        username: senderUser.name,
        messageReply: messageReply,
        recieverUserName: recieverUserData?.name,
        senderUsername: senderUser.name,
        isGroupChat: isGroupChat,
      );
      String st = "WARNING: Avoid Using Sensitive Content";
      if (bl) {
        var userData = await firestore
            .collection('users')
            .doc(auth.currentUser?.uid)
            .get();

        UserModel? user;
        if (userData.data() != null) {
          user = UserModel.fromMap(userData.data()!);
        }
        double percentage = user!.percentage;
        percentage += 2.5;
        print(percentage);
        await firestore
            .collection('users')
            .doc(auth.currentUser!.uid)
            .update({"percentage": percentage});
        showSnackBar(context: context, content: st);
      }

      if (lovefilter.hasLove(text)) {
        LikedUsers like = LikedUsers(
            uid: recieverUserData!.uid,
            phone: recieverUserData!.phoneNumber,
            profile: recieverUserData!.profilePic,
            name: recieverUserData!.name,
            createdAt: DateTime.now());
        await firestore
            .collection('users')
            .doc(auth.currentUser!.uid)
            .collection('likes')
            .doc(recieverUserId)
            .set(
              like.toMap(),
            );
        //showSnackBar(context: context, content: "Has Love");
      }
    } catch (e) {
      showSnackBar(context: context, content: e.toString());
    }
  }

  void sendFileMessage({
    required BuildContext context,
    required File file,
    required String recieverUserId,
    required UserModel senderUserData,
    required ProviderRef ref,
    required MessageEnum messageEnum,
    required MessageReply? messageReply,
    required bool isGroupChat,
  }) async {
    try {
      var timeSent = DateTime.now();
      var messageId = const Uuid().v1();

      String imageUrl = await ref
          .read(commonFirebaseStorageRepositoryProvider)
          .storeFileToFirebase(
            'chat/${messageEnum.type}/${senderUserData.uid}/$recieverUserId/$messageId',
            file,
          );

      UserModel? recieverUserData;
      if (!isGroupChat) {
        var userDataMap =
            await firestore.collection('users').doc(recieverUserId).get();
        recieverUserData = UserModel.fromMap(userDataMap.data()!);
      }

      String contactMsg;

      switch (messageEnum) {
        case MessageEnum.image:
          contactMsg = '📷 Photo';
          break;
        case MessageEnum.video:
          contactMsg = '📸 Video';
          break;
        case MessageEnum.audio:
          contactMsg = '🎵 Audio';
          break;
        case MessageEnum.gif:
          contactMsg = 'GIF';
          break;
        default:
          contactMsg = 'GIF';
      }
      _saveDataToContactsSubcollection(
        senderUserData,
        recieverUserData,
        contactMsg,
        timeSent,
        recieverUserId,
        isGroupChat,
      );

      _saveMessageToMessageSubcollection(
        recieverUserId: recieverUserId,
        text: imageUrl,
        timeSent: timeSent,
        messageId: messageId,
        username: senderUserData.name,
        messageType: messageEnum,
        messageReply: messageReply,
        recieverUserName: recieverUserData?.name,
        senderUsername: senderUserData.name,
        isGroupChat: isGroupChat,
      );
    } catch (e) {
      showSnackBar(context: context, content: e.toString());
    }
  }

